/**
 * Class to represent position of a single
 * queen on the board
 * @author Jakub Bachniak
 *
 */
public class SingleQueenPlacement {
	/**
	 * Fields
	 */
	private int row;
	private int col;

	public SingleQueenPlacement() {
	}

	public int getRow() {
		return row;
	}

	public void setRow(int row) {
		this.row = row;
	}

	public int getCol() {
		return col;
	}

	public void setCol(int col) {
		this.col = col;
	}
}